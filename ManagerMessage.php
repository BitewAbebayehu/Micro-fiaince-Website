<h1>General Manager's Message</h1>
<p>Dear Shareholders,</p>
		 <p>It is with great pleasure and sense of pride that I present to you Harbumfi annual performance report for the fiscal year just ended, June 30 2021. There have been scores of impactful initiatives undertaken by the Micro Finance during the year which meaningfully supported its continuous strive to offer improved services to its esteemed customers.  Revision and implementation of a new organization structure, upgrading of the Core-Micro Financeing system, design and implementation of a competitive pay scheme, finalization of the contract agreement for construction of the Micro Finance’s Headquarters building can be mentioned among them.  These wide-ranging endeavors of the Micro Finance had also been reinforced with a strong risk management and compliance practices which were undertaken all year through.  The Micro Finance has also been an active participant in several CSR activities lending a hand to various nationwide initiatives.</P>

<p>Looking ahead, as we conclude the just ended fiscal year with all of us as a nation still to work toward creating a peaceful and stable Ethiopia, and fight against the rise of the ‘third wave’ of COVID-19 which together are poised to impact our national growth trajectory, we shall remain resilient and hopeful to come out at the top of our challenges and stay on course to yet again register commendable achievements.  Despite facing headwinds of various forms, our Micro Finance is determined to perform even better by working to reinforce its growth strategy through enhancing its organizational capability and level of execution with the help and support of all its stakeholders as well as entire Micro Finance’s community.

I would like to congratulate the Board of Directors, shareholders, the Micro Finance’s management team as well as the entire staff for the hard work and effort exerted toward this year’s delightful achievement.  I would also like to take this opportunity to acknowledge the positive impacts and interventions made by the National Micro Finance of Ethiopia in navigating the industry toward a stable and safe Micro Financeing environment.  My sincere appreciations also goes to our customers and the entire business community who worked with us all year long and helped us serve them better.Harbu Microfinance Institution Share Company (Harbu) is an Ethiopian microfinance institution (MFI) based in Addis Ababa, Ethiopia. It assists farmers, especially women farmers, in the cultivation and processing of soybeans. As of 2009, Harbu had USD 1.3 million in total assets and a gross loan portfolio of USD 812,000. In 2008, Harbu reported return on assets (ROA) of 0.45 percent and return on equity (ROE) of 1.36 percent.
		Harbu Microfinance Institution Share Company (Harbu) of Ethiopia was recently presented the third annual “European Microfinance Award” by Maria Teresa, the Grand Duchess of Luxembourg, on behalf of the Luxembourg Round Table on Microfinance, a government organization established to catalyze interactions between microfinance institutions (MFIs) and financial institutions in Luxembourg, at the European Investment Micro Finance (EIB) in Luxembourg.
       The honor, awarded every two years by the Luxembourg Round Table on Microfinance, includes a prize of EUR 100,000 (USD 131,000). This year’s winner, Harbu, is an MFI that assists farmers in the cultivation and processing of soybeans. The organization currently supports 220 farmers, many of whom are women. Harbu’s CEO, Mr Tesfaye Befekadu, indicated that the money will be used to bring more women into the program. As of 2009, Harbu had USD 1.3 million in total assets and a gross loan portfolio of USD 812,000. In 2008, Harbu reported return on assets (ROA) of 0.45 percent and return on equity (ROE) of 1.36 percent [1].
